package com.example.proyecto2.views.AnimeDetalle;

public class AnimeDetallePrueba {
    private Integer langLogo;
    private String langName;

    public AnimeDetallePrueba(Integer langLogo,String langName){
        this.langLogo = langLogo;
        this.langName = langName;
    }

    public Integer getLangLogo() {
        return langLogo;
    }

    public String getLangName() {
        return langName;
    }
}
